            <h1 class="h1">About</h1>
            <h1 class="h3">The NOA Project</h1>
            <p>The aim of this project is to make scientific images more findable and easily reusable. In order to achieve this, we have extracted images from open access articles and indexed them in a search engine that is open to everyone. The images have been enriched with metadata that was gathered from the article text and various external sources.</p>
            <p>For further information refer to <a style="color:blue;text-decoration:underline;" href="https://zenodo.org/record/51562#.W-xAOTGNzmE" target="_blank">this article</a>, the <a style="color:blue;text-decoration:underline;" href="http://blogs.tib.eu/wp/noa/en/" target="_blank">project blog</a> or the various <a style="color:blue;text-decoration: underline;" href="https://blogs.tib.eu/wp/noa/en/publikationen/" target="_blank">publications from the project</a>.</p>

            <h2 class="h3">Wikimedia Commons</h2>	
            <p>Wikimedia Commons is a platform for freely reusable media. Images from the NOA project will be uploaded there to make them accessible to a larger audience and ensure sustainability. Anyone can help with choosing the right images to upload: Either use the <a style="color:blue;text-decoration:underline;" href="http://noa.wp.hs-hannover.de/random-upload.php" target="_blank">Upload Tool</a> which will show you random images with the option to upload them with a single click or use “Upload to Wikimedia” button once you have found an interesting image in your search results.</p>

            <h2 class="h3">Search engine</h2>	
            <p>Currently we have indexed over 4.5 Million images from open access journals published by various publishers. Images can be filtered by discipline and image type. They are also annotated with Wikipedia categories as keywords.</p>
            <p>All images are from open access journals. However, before using any images it is your own responsibility to check the conditions to use the image. The offered service is completely experimental and we take no guarantee for proper functionality and no responsibility for using the service.</p>
